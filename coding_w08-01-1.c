#include <stdio.h>

int main() {
    int i = 2;

    while (i <= 10) {
        printf("%d ", i);
        i += 2;
    }
    printf("\nEnd of loop\n");
    return 0;
}
// while loop จะทำงานตราบใดที่เงื่อนไขเป็นจริง
// ในขณะที่เงื่อนไขเป็นจริง จะทำงานในบล็อกของลูป
// เมื่อเงื่อนไขเป็นเท็จ จะออกจากลูปและทำงานต่อไปยังคำสั่งถัดไป